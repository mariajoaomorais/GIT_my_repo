package academy.mindswap;

public class Calculator2 {

    int sum;
    int subtract;
    int multiply;
    int divide;

    public int sum(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public int divide (int a, int b){
        return a / b;
    }

}
